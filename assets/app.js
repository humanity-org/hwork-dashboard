/* Hwork dashboard — client renderer.
   This file is FIXED presentation code. It only READS data files; it is never
   regenerated by the agent. The agent/Action updates data/*.json only. */

const $ = (id) => document.getElementById(id);

function showError(msg) {
  const b = $("error-banner");
  b.hidden = false;
  b.textContent = msg;
}

function esc(s) {
  return String(s == null ? "" : s).replace(/[&<>"']/g, (c) => ({
    "&": "&amp;", "<": "&lt;", ">": "&gt;", '"': "&quot;", "'": "&#39;",
  }[c]));
}

async function loadJSON(path) {
  const res = await fetch(path, { cache: "no-store" });
  if (!res.ok) throw new Error(`${path}: HTTP ${res.status}`);
  return res.json();
}

/* ---------- renderers ---------- */

function renderCounts(d) {
  const s = d.summary || {};
  const stats = [
    { label: "Open Issues", num: s.open ?? 0, cls: "" },
    { label: "P0 Critical", num: s.p0 ?? 0, cls: "p0" },
    { label: "P1 High", num: s.p1 ?? 0, cls: "p1" },
    { label: "P2", num: s.p2 ?? 0, cls: "p2" },
    { label: "Blocked", num: (d.blocked || []).length, cls: "p0" },
    { label: "Needs Owner", num: (d.needsOwner || []).length, cls: "" },
  ];
  $("stat-grid").innerHTML = stats.map((x) =>
    `<div class="stat ${x.cls}"><div class="num">${x.num}</div><div class="label">${esc(x.label)}</div></div>`
  ).join("");

  const byStatus = d.byStatus || {};
  const chips = Object.keys(byStatus).map((k) =>
    `<span class="status-chip"><span class="dot"></span>${esc(k)} <b>${byStatus[k]}</b></span>`
  ).join("");
  $("status-breakdown").innerHTML = chips || `<span class="empty">No status data</span>`;
}

function issueLi(it, opts = {}) {
  const pr = it.priority ? `<span class="tag ${it.priority.toLowerCase()}">${esc(it.priority)}</span>` : "";
  const owner = opts.showOwner !== false
    ? `<span class="meta">${it.owner ? "👤 " + esc(it.owner) : "— unassigned"}</span>` : "";
  return `<li>${pr}<a href="${esc(it.url)}" target="_blank" rel="noopener">#${esc(it.number)}</a>`
       + `<span class="title">${esc(it.title)}</span>${owner}</li>`;
}

function renderP0(d) {
  const list = (d.p0List || []);
  $("p0-count").textContent = (d.summary && d.summary.p0) ?? list.length;
  $("p0-list").innerHTML = list.length
    ? list.map((it) => issueLi(it)).join("")
    : `<li class="empty">No open P0 issues 🎉</li>`;
}

function renderBlocked(d) {
  const rows = (d.blocked || []);
  $("blocked-count").textContent = rows.length;
  const tbody = $("blocked-table").querySelector("tbody");
  if (!rows.length) {
    tbody.innerHTML = `<tr><td colspan="5" class="empty">Nothing blocked 🎉</td></tr>`;
    return;
  }
  tbody.innerHTML = rows.map((it) => {
    const sla = it.slaDays != null ? it.slaDays : null;
    const age = it.ageDays != null ? it.ageDays : null;
    let slaCell = "—";
    if (sla != null && age != null) {
      const breach = age > sla;
      slaCell = `<span class="${breach ? "sla-breach" : "sla-ok"}">${sla}d ${breach ? "⚠ over" : "ok"}</span>`;
    }
    return `<tr>
      <td><a href="${esc(it.url)}" target="_blank" rel="noopener">#${esc(it.number)}</a> ${esc(it.title)}</td>
      <td>${esc(it.blockedType || "blocked")}</td>
      <td>${it.owner ? esc(it.owner) : "<span class='empty'>none</span>"}</td>
      <td>${age != null ? age + "d" : "—"}</td>
      <td>${slaCell}</td>
    </tr>`;
  }).join("");
}

function renderNeedsOwner(d) {
  const list = (d.needsOwner || []);
  $("needs-owner-count").textContent = list.length;
  $("needs-owner-list").innerHTML = list.length
    ? list.map((it) => issueLi(it, { showOwner: false })).join("")
    : `<li class="empty">Every active issue has an owner ✓</li>`;
}

function renderReadiness(r) {
  if (r.title) $("readiness-title").textContent = r.title;
  if (r.updated) $("readiness-updated").textContent = "Checklist updated: " + r.updated;
  const phases = (r.phases || []);
  const symbol = { done: "✓", progress: "◐", blocked: "✕", todo: "○" };
  $("phases").innerHTML = phases.map((p) => {
    const items = (p.items || []).map((i) => {
      const st = (i.status || "todo").toLowerCase();
      return `<li><span class="s s-${st}">${symbol[st] || "○"}</span>`
           + `<span>${esc(i.label)}</span>`
           + `${i.owner ? `<span class="who">${esc(i.owner)}</span>` : ""}</li>`;
    }).join("");
    return `<div class="phase"><h3>${esc(p.name)}</h3>`
         + `${p.summary ? `<p class="phase-sub">${esc(p.summary)}</p>` : ""}`
         + `<ul>${items}</ul></div>`;
  }).join("") || `<p class="empty">No readiness checklist yet.</p>`;
}

/* ---------- boot ---------- */

async function main() {
  // Issues data is required; readiness is optional.
  try {
    const d = await loadJSON("./data/issues.json");
    $("last-updated").textContent = "Last updated: " + (d.generatedAt || "unknown");
    renderCounts(d);
    renderP0(d);
    renderBlocked(d);
    renderNeedsOwner(d);
  } catch (e) {
    showError("Could not load issue data (" + e.message + "). The board data file may not have been generated yet.");
  }

  try {
    const r = await loadJSON("./data/readiness.json");
    renderReadiness(r);
  } catch (e) {
    $("phases").innerHTML = `<p class="empty">Readiness checklist not available.</p>`;
  }
}

main();
